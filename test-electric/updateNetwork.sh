# This script helps creating BNA file installing it on the Peer and Upgrading the network to a newer version






PACKAGE_VERSION=$(cat package.json \
  | grep version \
  | head -1 \
  | awk -F: '{ print $2 }' \
  | sed 's/[",]//g' \
  | tr -d '[[:space:]]')

echo $PACKAGE_VERSION

composer archive create -t dir -n .
composer network install --card PeerAdmin@hlfv1 --archiveFile test-electric@$PACKAGE_VERSION.bna
composer network upgrade -c PeerAdmin@hlfv1 -n test-electric -V $PACKAGE_VERSION
